import UIKit.UIColor

extension UIColor {
    
    static func gradientFirst() -> UIColor {
        
        return UIColor(red: CGFloat(0) / 255.0, green: CGFloat(133) / 255.0, blue: CGFloat(255) / 255.0, alpha: 1.0)
    }
    
    static func gradientSecond() -> UIColor {
        
        return UIColor(red: CGFloat(105) / 255.0, green: CGFloat(165) / 255.0, blue: CGFloat(235) / 255.0, alpha: 0)
    }
    
    static func mainBlack() -> UIColor {
        
        return UIColor(red: CGFloat(40) / 255.0, green: CGFloat(42) / 255.0, blue: CGFloat(46) / 255.0, alpha: 1.0)
    }
    
    static func alphaBlack() -> UIColor {
        
        return UIColor(red: CGFloat(40) / 255.0, green: CGFloat(42) / 255.0, blue: CGFloat(46) / 255.0, alpha: 128)
    }
    
    static func mainBlue() -> UIColor {
        
        return UIColor(red: CGFloat(0) / 255.0, green: CGFloat(115) / 255.0, blue: CGFloat(230) / 255.0, alpha: 1.0)
    }
    
    static func mainGray() -> UIColor {
        
        return UIColor(red: CGFloat(208) / 255.0, green: CGFloat(208) / 255.0, blue: CGFloat(208) / 255.0, alpha: 1.0)
    }
    
    static func topViewFGradient() -> UIColor {
        
        return UIColor(red: 0, green: 0.478, blue: 1, alpha: 1.0)
    }
    
    static func topViewSGradient() -> UIColor {
        
        return UIColor(red: 0.41, green: 0.648, blue: 0.92, alpha: 1.0)
    }
    
    
}
